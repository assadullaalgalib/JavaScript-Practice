// JavaScript Practice File - Variables and Data Types

// 1. String
let name = "Alice";
console.log("Name:", name);

// 2. Number
let age = 25;
console.log("Age:", age);

// 3. Boolean
let isStudent = true;
console.log("Is Student:", isStudent);

// 4. Undefined
let grade;
console.log("Grade (undefined):", grade);

// 5. Null
let course = null;
console.log("Course (null):", course);

// 6. Array
let hobbies = ["Reading", "Coding", "Music"];
console.log("Hobbies:", hobbies);

// 7. Object
let user = {
  name: "Alice",
  age: 25,
  city: "New York"
};
console.log("User Info:", user);

// 8. typeof checks
console.log("Type of name:", typeof name);
console.log("Type of age:", typeof age);
console.log("Type of isStudent:", typeof isStudent);
console.log("Type of grade:", typeof grade);
console.log("Type of course:", typeof course);
console.log("Type of hobbies:", typeof hobbies);
console.log("Type of user:", typeof user);
